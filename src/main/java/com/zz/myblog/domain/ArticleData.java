package com.zz.myblog.domain;

public class ArticleData {
	private String uid;
	private String article_title;
	private String editor_data;
	private String article_date;
	private String article_cat;
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getArticle_title() {
		return article_title;
	}
	public void setArticle_title(String article_title) {
		this.article_title = article_title;
	}
	public String getEditor_data() {
		return editor_data;
	}
	public void setEditor_data(String editor_data) {
		this.editor_data = editor_data;
	}
	public String getArticle_cat() {
		return article_cat;
	}
	public void setArticle_cat(String article_cat) {
		this.article_cat = article_cat;
	}
	public String getArticle_date() {
		return article_date;
	}
	public void setArticle_date(String article_date) {
		this.article_date = article_date;
	}
	
	
}
